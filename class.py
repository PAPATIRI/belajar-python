class kalkulator:
    def pertambahan(a,b):
        hasil=a+b
        print(hasil)
    def pengurangan(a,b):
        hasil=a-b
        print(hasil)
    def perkalian(x,y):
        hasil=x*y
        print(hasil)
    def pembagian(x,y):
        hasil=x/y
        print(hasil)

#pemanggilan method" dalam kelas sebagai berikut
kalkulator.pertambahan(2,1)
kalkulator.pengurangan(2,1)
kalkulator.perkalian(2,3)
kalkulator.pembagian(2,1)